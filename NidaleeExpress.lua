        	if self.Menu.ClearMode.UseR:Value() and minion and myHero:GetSpellData(_E).name == "Swipe" then
            	if not self:CanCast(_Q) and not self:CanCast(_E) and not self:CanCast(_W) then
					if Game.Timer() - LastR > 8 then
			    		Control.CastSpell(HK_R)
					end
            	end
        	end
    	end

    	if self:CanCast(_R) then 
        	if self.Menu.ClearMode.UseR:Value() and minion and myHero:GetSpellData(_E).name == "Swipe" then
            	if myHero.health/myHero.maxHealth < .30 and myHero.pos:DistanceTo(minion.pos) > 700 then
			    	Control.CastSpell(HK_R)
            	end
        	end
    	end

	end
	end
